import math

a = 10.3
b = math.ceil(a)
print("ceil function will convert the floating value to next integer value.")
print(b,"\n")

c = -10
d = math.fabs(c)
print("fabs return absolute value")
print(d,"\n")

print("factorial return factorial of number")
facto = math.factorial(b)
print(facto,"\n")

print("floor function will convert the floating value to previous integer value.")
e = math.floor(10.5)
print(e,"\n")

print("fsum return sum of an itrable")
list_sum = math.fsum([10,20,30])
print(list_sum,"\n")

print("sqrt find the square root of a number")
sqroot = math.sqrt(9)
print(sqroot,"\n")