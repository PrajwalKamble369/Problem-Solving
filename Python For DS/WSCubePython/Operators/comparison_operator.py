# declaring variables
x = 10
y = 15

# equal to operator (==)
print("Equal to Operator(==)", x == 10, "\n")

# not equal to operator (!=)
print("Not Equal to Operator(!=)",x!=y,"\n")

# greater than operator (>)
print("Greater than Operator(>)",x>y,"\n")

# less than operator (<)
print("Less than Operator(<)",x<y, "\n")

# greater than or equal to (>=)
print("Greater than or Equal to(>=)",x>=y, "\n")

# less than or equal to (<=)
print("Less than or Equal to(<=)",x<=y)